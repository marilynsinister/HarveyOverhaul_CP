def replace_values(body, transform_fn, extra=None):
    entries = dict(re.findall(r'"([^"]+)":\s*"((?:[^"\\]|\\.)*)"', body))
    if extra:
        entries.update(extra)
    for key in list(entries.keys()):
        entries[key] = transform_fn(key, entries[key])
    out = body
    for key, val in entries.items():
        pat = rf'"{re.escape(key)}":\s*"((?:[^"\\]|\\.)*)"'
        if re.search(pat, out):
            out = re.sub(pat, f'"{key}": "{esc(val)}"', out, count=1)
    return out


def extract_block(text, marker):
    i = text.find(marker)
    if i < 0:
        return ""
    start = text.rfind("{", 0, i)
    end = text.find("\n        },", i)
    return text[start : end + len("\n        },")]


def load_git_source():
    r = subprocess.run(
        ["git", "-C", str(REPO), "show", "HEAD:assets/Code/dialoguesHarveyCure.json"],
        capture_output=True,
        text=True,
        encoding="utf-8",
    )
    if r.returncode != 0:
        raise RuntimeError(r.stderr or "git show failed")
    return r.stdout


def extract_first_entries(text):
    base_start = text.find('"Entries": {')
    base_end = text.find("\n        },\n        {", base_start)
    if base_end < 0:
        base_end = text.find("\n        }\n    ]", base_start)
    base_body = text[base_start:base_end]
    return base_body[base_body.find("{") + 1 : base_body.rfind("}")]


